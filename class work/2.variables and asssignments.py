#basic Assignment
a=10
b=20
print(a)
print(b)

#Multiple Assignment
a,b,c=100,200,300
print(a)
print(b)
print(c)

#Same value Assignment
a=b=c=100
print(a)
print(b)
print(c)

#swapping variable
a=50
b=60
a,b=b,a
print(a)
print(b)

#Deleting Assignment
a=10
b=20
del(a)
print(b)
